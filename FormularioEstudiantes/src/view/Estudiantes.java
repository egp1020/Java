    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String matricula = txtMatricula.getText();
        String nombre = txtNombre.getText();
        int edad = Integer.parseInt(txtEdad.getText());
        String email = txtEmail.getText();
        String sexo;
        if(rbFemenino.isSelected()==true){
            sexo = "F";
        }else if(rbMasculino.isSelected()==true){
            sexo = "M";
        } else{
            sexo = "NN";
        }
        
        try{
         Connection conn = Coneccion.getConnection();
         PreparedStatement ps = conn.prepareStatement("INSERT INTO estudiantes_nuevo(matricula, nombre, edad, sexo, email, activo) VALUES(?,?,?,?,?,?)");
         
         ps.setString(1, matricula);
         ps.setString(2, nombre);
         ps.setInt(3, edad);
         ps.setString(4, sexo);
         ps.setString(5, email);
         ps.setInt(6, 1);
         ps.executeUpdate();
         
         limpiar();
         JOptionPane.showMessageDialog(null, "Registro ha sido guardado");
         cargarTabla();
         
        } catch(Exception e){
            System.out.println("Error en la conexión: "+e.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

