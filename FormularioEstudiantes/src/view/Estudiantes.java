    private void cargarTabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) tbEstudiantes.getModel();
        modeloTabla.setRowCount(0);
        
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columnas;
        int[] anchos = {10, 50, 70, 30, 100};
        
        for(int i = 0; i<tbEstudiantes.getColumnCount();i++){
            tbEstudiantes.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }
        
        try{
            Connection conn = Coneccion.getConnection();
            ps = conn.prepareStatement("SELECT id, matricula, nombre, sexo, email from estudiantes_nuevo");
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();
            
            while(rs.next()){
                Object[] fila = new Object[columnas];
                for(int indice=0; indice<columnas;indice++){
                    fila[indice] = rs.getObject(indice+1);
                }
                modeloTabla.addRow(fila);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }
