    private void tbEstudiantesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbEstudiantesMouseClicked
       try{
           int fila = tbEstudiantes.getSelectedRow();
           int id = Integer.parseInt(tbEstudiantes.getValueAt(fila, 0).toString());
           PreparedStatement ps;
           ResultSet rs;
           
           Connection conn = Coneccion.getConnection();
           ps = conn.prepareStatement("SELECT matricula, nombre, edad, sexo, email FROM estudiantes_nuevo WHERE id=?");
           ps.setInt(1, id);
           rs = ps.executeQuery();
           
           while(rs.next()){
               txtId.setText(String.valueOf(id));
               txtMatricula.setText(rs.getString("matricula"));
               txtNombre.setText(rs.getString("nombre"));
               txtEdad.setText(rs.getString("edad"));
               txtEmail.setText(rs.getString("email"));
               if (rs.getString("sexo").equals("F")){
                   rbFemenino.setSelected(true);
               }else if(rs.getString("sexo").equals("M")){
                    rbMasculino.setSelected(true);
               }
           }
           
       }catch(Exception e){
           System.out.println("Error: "+e.toString());
       }
    }//GEN-LAST:event_tbEstudiantesMouseClicked

